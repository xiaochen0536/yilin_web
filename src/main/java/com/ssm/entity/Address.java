package com.ssm.entity;

public class Address {
	private Integer did;
	private String utname;
	private String utaddress1;
	private String utaddress2;
	private Integer utmail;
	private String uttel;
	private Integer sid;
	public Integer getDid() {
		return did;
	}
	public void setDid(Integer did) {
		this.did = did;
	}
	public String getUtname() {
		return utname;
	}
	public void setUtname(String utname) {
		this.utname = utname;
	}
	public String getUtaddress1() {
		return utaddress1;
	}
	public void setUtaddress1(String utaddress1) {
		this.utaddress1 = utaddress1;
	}
	public String getUtaddress2() {
		return utaddress2;
	}
	public void setUtaddress2(String utaddress2) {
		this.utaddress2 = utaddress2;
	}
	public Integer getUtmail() {
		return utmail;
	}
	public void setUtmail(Integer utmail) {
		this.utmail = utmail;
	}
	public String getUttel() {
		return uttel;
	}
	public void setUttel(String uttel) {
		this.uttel = uttel;
	}
	public Integer getSid() {
		return sid;
	}
	public void setSid(Integer sid) {
		this.sid = sid;
	}
	
}
